public class A04 {
    public static void main(String[] args) {
        int x = 7 / 2; // int -> int
        float y = 7 / 2; // int -> float
        float z = 7 / 2.0f; // float -> float

        System.out.printf("%d\n", x);
        System.out.printf("%f\n", y);
        System.out.printf("%f\n", z);
    }
}
